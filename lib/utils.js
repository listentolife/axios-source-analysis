/*global toString:true*/

// utils is a library of generic helper functions non-specific to axios

// 提取Object上的toString方法
var toString = Object.prototype.toString;

/**
 * Determine if a value is an Array
 *
 * @param {Object} val The value to test
 * @returns {boolean} True if value is an Array, otherwise false
 * 
 * 使用Object上的toString方法判断变量是否为数组
 */
function isArray(val) {
  return toString.call(val) === '[object Array]';
}

/**
 * Determine if a value is undefined
 *
 * @param {Object} val The value to test
 * @returns {boolean} True if the value is undefined, otherwise false
 * 
 * 使用typeof判断变量是否为undefined
 */
function isUndefined(val) {
  return typeof val === 'undefined';
}

/**
 * Determine if a value is a Buffer
 *
 * @param {Object} val The value to test
 * @returns {boolean} True if value is a Buffer, otherwise false
 * 
 * buffer其实可以用Buffer.isBuffer(val)来判断
 * 如果想从val上“自我认证”，则需要判断val不是null，不是undefined，然后typof为object，
 * 然后其constructor不为null，不为undefined，constructor上isBuffer为函数且传入val返回true
 */
function isBuffer(val) {
  return val !== null && !isUndefined(val) && val.constructor !== null && !isUndefined(val.constructor)
    && typeof val.constructor.isBuffer === 'function' && val.constructor.isBuffer(val);
}

/**
 * Determine if a value is an ArrayBuffer
 *
 * @param {Object} val The value to test
 * @returns {boolean} True if value is an ArrayBuffer, otherwise false
 * 
 * 使用Object上的toString方法判断变量是否为ArrayBuffer
 * ArrayBuffer 表示通用的、固定长度的原始二进制数据缓冲区
 */
function isArrayBuffer(val) {
  return toString.call(val) === '[object ArrayBuffer]';
}

/**
 * Determine if a value is a FormData
 *
 * @param {Object} val The value to test
 * @returns {boolean} True if value is an FormData, otherwise false
 * 
 * 判断变量是否为FormData的实例
 */
function isFormData(val) {
  return (typeof FormData !== 'undefined') && (val instanceof FormData);
}

/**
 * Determine if a value is a view on an ArrayBuffer
 *
 * @param {Object} val The value to test
 * @returns {boolean} True if value is a view on an ArrayBuffer, otherwise false
 * 
 * 判断变量是否为ArrayBuffer的视图
 * 先判断ArrayBuffer类型是否存在，是则使用ArrayBuffer.isView来判断
 * 否则判断变量是否有值，变量上是否有buffer属性且为ArrayBuffer的实例
 */
function isArrayBufferView(val) {
  var result;
  if ((typeof ArrayBuffer !== 'undefined') && (ArrayBuffer.isView)) {
    result = ArrayBuffer.isView(val);
  } else {
    result = (val) && (val.buffer) && (val.buffer instanceof ArrayBuffer);
  }
  return result;
}

